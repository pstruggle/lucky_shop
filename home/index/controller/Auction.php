    // 从购物车选中商品购买
    public function buy(){
        if (!$this->request->isPost()){
            return $this->error('请求错误');
        }
        $data = input('post.');
        if(empty($data['carts'])){
            return $this->error('请选择购买商品');
        }
        $where = ['uid'=>$this->user['id'],'id'=>['in',$data['carts']]];
        $_cart = model('cart');
        $carts = $_cart->get_carts($where);
        $this->assign([
            'title' => get_cache('config.mall')['store_title'],
            'carts' => $carts,
        ]);
        return $this->template();
    }
